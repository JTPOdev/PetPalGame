public class User {
    
    public string name;
    public string health;
    public string hunger;
    public string bath;
    public string thirst;
    public int gold;

    public User (string name, string health, string hunger, string bath, string thirst, int gold)
    {
        this.name = name;
        this.health = health;
        this.hunger = hunger;
        this.bath = bath;
        this.thirst = thirst;
        this.gold = gold;
    }
}